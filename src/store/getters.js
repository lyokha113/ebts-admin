const getters = {
  // ////////////////////////////////////////////
  // APP
  // ////////////////////////////////////////////
  sidebarWidth: state => state.sidebarWidth,

  // ////////////////////////////////////////////
  // USER
  // ////////////////////////////////////////////
  accessToken: state => state.accessToken,
  activeUser: state => state.activeUser,
  permission: state => state.activeUser && state.activeUser.roleId,

  // ////////////////////////////////////////////
  // ACCOUNT
  // ////////////////////////////////////////////
  accounts: state => state.accounts,

  // ////////////////////////////////////////////
  // CATEGORY
  // ////////////////////////////////////////////
  categoriesNoTemplate: state => state.categoriesNoTemplate,
  categoriesWithTemplates: state => state.categoriesWithTemplates,

  // ////////////////////////////////////////////
  // TEMPLATES
  // ////////////////////////////////////////////
  templates: state => state.templates,
  templatesByAuthor: state => state.templatesByAuthor,
  currentTemplate: state => state.currentTemplate,

  // ////////////////////////////////////////////
  // FILE
  // ////////////////////////////////////////////
  files: state => state.files,
  editorFiles: state =>
    state.files &&
    state.files
      .filter(f => f.active)
      .map(f => {
        return {
          id: f.id,
          name: f.name,
          src: f.link,
          type: 'image'
        }
      }),

  // ////////////////////////////////////////////
  // TUTORIAL
  // ////////////////////////////////////////////
  tutorials: state => state.tutorials,

  // ////////////////////////////////////////////
  // PUBLISH
  // ////////////////////////////////////////////
  publishes: state => state.publishes,

  // ////////////////////////////////////////////
  // WORKSPACE
  // ////////////////////////////////////////////
  workspaces: state => state.workspaces,

  // ////////////////////////////////////////////
  // USER EMAIL
  // ////////////////////////////////////////////
  userEmails: state => state.userEmails,

  // ////////////////////////////////////////////
  // USER BLOCK
  // ////////////////////////////////////////////
  userBlocks: state => state.userBlocks,
  currentBlock: state => state.currentBlock,

  // ////////////////////////////////////////////
  // DESIGN SESSION
  // ////////////////////////////////////////////
  sessions: state => state.sessions,
  contributors: state => state.contributors,
  sessionFiles: state =>
    state.sessionFiles &&
    state.sessionFiles.map(f => {
      return {
        id: f.id,
        name: f.name,
        src: f.link,
        type: 'image'
      }
    }),

  // ////////////////////////////////////////////
  // EDITOR
  // ////////////////////////////////////////////
  editorRawId: state => state.editorRawId,
  editorOwnerId: state => state.editorOwnerId,
  editorContent: state => state.editorContent,
  editorChange: state => state.editorChange,
  forceKick: state => state.forceKick
}

export default getters
